import type { PaymentInput } from "../types/payment.js";
import { buildPaymentQuery } from "../utils/http.js";

export type BillLanguage = "DE" | "EN";

export interface RenderedBillLinks {
  apiJsonUrl: string;
  embedSvgUrl: string;
  pdfUrl: string;
  svgUrl: string;
  previewUrl: string;
}

export interface RenderedBillMetadata {
  generatedAt: string;
  input: PaymentInput;
  links: RenderedBillLinks;
  summary: Array<{ label: string; value: string }>;
}

